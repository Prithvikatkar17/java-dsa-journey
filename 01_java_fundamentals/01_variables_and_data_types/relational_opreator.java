public class relational_opreator {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println("Value of a: " + a);
        System.out.println("Value of b: " + b);

        // Relational operators
        System.out.println("a == b: " + (a == b));
        System.out.println("a != b: " + (a != b));
        System.out.println("a > b: " + (a > b));
        System.out.println("a < b: " + (a < b));
        System.out.println("a >= b: " + (a >= b));
        System.out.println("a <= b: " + (a <= b));
    }
    
}
